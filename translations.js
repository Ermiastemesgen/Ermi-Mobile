// Current language (default: English)
let currentLanguage = localStorage.getItem('language') || 'en';

// Get translation
function t(key) {
    return translations[currentLanguage][key] || translations.en[key] || key;
}

// Switch language
function switchLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    updatePageLanguage();
}

// Update all text on page
function updatePageLanguage() {
    // Update HTML lang attribute
    document.documentElement.lang = currentLanguage;
    
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = t(key);
        
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
            element.placeholder = translation;
        } else {
            element.textContent = translation;
        }
    });
    
    // Update language toggle button
    const langToggle = document.getElementById('languageToggle');
    if (langToggle) {
        langToggle.innerHTML = currentLanguage === 'en' 
            ? '<i class="fas fa-language"></i> አማ' 
            : '<i class="fas fa-language"></i> EN';
        langToggle.title = currentLanguage === 'en' ? 'Switch to Amharic' : 'Switch to English';
    }
    
    // Trigger custom event for dynamic content
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: currentLanguage } }));
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    updatePageLanguage();
});

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { t, switchLanguage, currentLanguage, translations };
}
